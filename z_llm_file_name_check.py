def test_file_name_check(file_name_check):
    assert file_name_check('file_name.txt') == 'Yes'
    assert file_name_check('file_name.exe') == 'Yes'
    assert file_name_check('file_name.dll') == 'Yes'
    assert file_name_check('file_name.txt.exe') == 'Yes'
    assert file_name_check('file_name.txt.dll') == 'Yes'
    assert file_name_check('file_name.exe.dll') == 'Yes'
    assert file_name_check('file_name.exe.txt') == 'Yes'
    assert file_name_check('file_name.exe.dll.txt') == 'Yes'
    assert file_name_check('file_name.exe.dll.exe.txt') == 'Yes'
    assert file_name_check('file_name.exe.dll.exe.exe.txt') == 'Yes'
    assert file_name_check('file_name.exe.dll.exe.exe.exe.txt') == 'Yes'
    assert file_name_check('file_name.exe.dll.exe.exe.exe.exe.txt') == 'Yes'
    assert file_name_check('file_name.exe.dll.exe.exe.exe.exe.exe.txt') == 'Yes'
    assert file_name_check('file_name.exe.dll.exe.exe.exe.exe.exe.txt') == 'Yes'
    assert file_name_check('file_name.exe.dll.exe.exe.exe.exe.exe.txt') == 'Yes'
    assert file_name_check('file_name.exe.dll.exe.exe.exe.exe.exe.txt') == 'Yes'
    assert file_name_check('file_name.exe.dll.exe.exe.exe.exe.exe.txt') == 'Yes'
    assert file_name_check('file_name.exe.dll.exe.exe.exe.exe.exe.txt') == 'Yes'
    assert file_name_check('file_name.exe.dll.exe.exe.exe.exe.exe.txt') == 'Yes'
